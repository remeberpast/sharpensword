#define _CRT_SECURE_NO_WARNINGS 1

#include "game.h"
void menu()
{
	printf("*****************************\n");
	printf("**********  1.play  *********\n");
	printf("**********  0.exit  *********\n");
	printf("*****************************\n");
}
void game()
{
	//存储数据，二维数组
	char board[ROW][COL] = { 0 };
	//初始化棋盘，初始化空格
	InitBoard(board, ROW, COL);
	//打印棋盘,本质就是打印出数组的内容
	DisplayBoard(board, ROW, COL);
	//游戏就包括玩家走，电脑走
	char ret = 0;     //接收游戏状态
	while (1)
	{
		//玩家下棋
		PlayerMove(board, ROW, COL);
		//下过棋后就打印一次棋盘
		DisplayBoard(board, ROW, COL);
		//判断玩家是否赢得游戏
		ret = IsWin(board, ROW, COL);   //iswin有四种情况，玩家赢（*），电脑赢（）#，平局（Q）或游戏继续(C)，故此处用四个符号代替四种情况,只有返回C时游戏才会继续
		if (ret != 'C')
			break;
		 //电脑下棋									
		CommputerMove(board, ROW, COL);
		//下过棋后就打印一次棋盘
		DisplayBoard(board, ROW, COL);
		//判断电脑是否赢得游戏
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
			break;
	}
	if (ret == '*')
	{
		printf("玩家赢了\n");
		DisplayBoard(board, ROW, COL);
	}
	else if (ret == '#')
	{
		printf("电脑赢了\n");
		printf("人机都打不过？SB是吧\n");
		DisplayBoard(board, ROW, COL);
	}
	else
	{
		printf("平局\n");
		DisplayBoard(board, ROW, COL);
	}
	//这里可以优化一下，因为上面三种情况都要打印棋盘，所以在最后打印一次即可
	//DisplayBoard(board, ROW, COL);
}
int main(void)
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择：");
		scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏\n");
			break;
		default:
			printf("选择错误，请重新选择（1/0）\n");
			break;
		}
	} while (input);     //巧妙的使用了input>1时判断为真，input=0时判断为假
	return 0;
}